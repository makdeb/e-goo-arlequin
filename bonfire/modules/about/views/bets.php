<p><ul>
	<li>П1 - Победа Команды 1.</li>
	<li>П2 - Победа Команды 2.</li>
	<li>Х - Ничья.</li>
	<li>1Х - Победа Команды 1 или ничья. Для выигрыша по этой ставке необходимо, чтобы победила Команда 1 или произошла ничья.</li>
	<li>Х2 - Победа Команды 2 или ничья. Для выигрыша по этой ставке необходимо, чтобы победила Команда 2 или произошла ничья.</li>
	<li>12 - Победа Команды 1 или победа Команды 2. Для выигрыша по ставке на такой исход необходимо, чтобы не было ничьей.</li>
	
</ul>
