<p> Подтверждение платежа. Если все правильно, Вы нажимаете кнопку «Оплатить». О том, что средства переведены Вы получите SMS – сообщение.</p>

<p>После оплаты Вы сможете просматривать VIP-прогнозы в системе EasyVictory.</p>

<p><b style="color:red;">Обратите внимание!!!</b><br/>
Для совершения платежей через систему liqPay необходимо подключить банковскую карту к счету, иначе система будет постоянно отображать ошибку.<br/>
Для этого зайдите на сайт liqpay.com, введите номер телефона и одноразовый пароль, который приходит в SMS – сообщении на указанный номер.
Войдите в меню «Mой Liqpay» - «Подлючить карту» (в списке слева). Введите номер карты, срок действия и CVV2 / CVC2. На карте будет заблокирована сумма от 0.01 до 3.00 единиц в выбранной валюте. Для верификации карты Вам необходимо уточнить заблокированную сумму в вашем Интернет банке или мобильном банке и ввести ее в соответствующее поле. После подключения карты Вы сможете без проблем совершать платежи посредством системы liqPay и банковской карты.</p>

