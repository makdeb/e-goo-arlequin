<p><h4>Оплата банковской картой VISA/MASTERCARD</h4> </p>

<p>Для оплаты прогноза Вы должны быть зарегистрированы в нашей системе. После регистрации выберите подписку на период 1 день, 7 дней, 15 дней,1 месяц, или же выберите отдельный прогноз, после чего нажмите "Оплата".<br/>
На сайте INTERKASSA выберите способ оплаты «СНГ VISA/MASTERCARD», после чего нажмите "Оплатить".</p>

